package az.developia.demo.Entity;

import az.developia.demo.Service.CustomUserDetailsService;
import az.developia.demo.Utility.JwtFilter;
import az.developia.demo.Utility.JwtUtil;
import lombok.RequiredArgsConstructor;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.http.HttpMethod;
import org.springframework.security.authentication.AuthenticationManager;
import org.springframework.security.authentication.AuthenticationProvider;
import org.springframework.security.authentication.dao.DaoAuthenticationProvider;
import org.springframework.security.config.annotation.authentication.configuration.AuthenticationConfiguration;
import org.springframework.security.config.annotation.method.configuration.EnableMethodSecurity;
import org.springframework.security.config.annotation.web.builders.HttpSecurity;
import org.springframework.security.config.annotation.web.configuration.EnableWebSecurity;
import org.springframework.security.config.http.SessionCreationPolicy;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.crypto.bcrypt.BCryptPasswordEncoder;
import org.springframework.security.crypto.password.PasswordEncoder;
import org.springframework.security.web.SecurityFilterChain;
import org.springframework.security.web.authentication.UsernamePasswordAuthenticationFilter;
import org.springframework.web.cors.CorsConfiguration;
import org.springframework.web.cors.CorsConfigurationSource;
import org.springframework.web.cors.UrlBasedCorsConfigurationSource;

import java.util.List;

@Configuration
@EnableWebSecurity
@RequiredArgsConstructor
@EnableMethodSecurity(prePostEnabled = true)
public class SecurityConfig {
    private final JwtFilter jwtFilter;
    private final CustomUserDetailsService userDetailsService;


    @Bean
    public SecurityFilterChain securityFilterChain(HttpSecurity http) throws Exception {
        http.csrf(csrf -> csrf.disable())
                .cors(cors -> {})
                .authorizeHttpRequests(auth -> auth
                        .requestMatchers(HttpMethod.GET, "/api/users/getAll").hasAuthority("ROLE_ADMIN")
                        .requestMatchers(HttpMethod.POST, "/api/users/**").permitAll()
                        .requestMatchers(HttpMethod.DELETE, "/api/users/delete/{id}").permitAll()
                        .requestMatchers(HttpMethod.PUT, "/api/users/{id}").permitAll()
                        .requestMatchers(HttpMethod.GET, "/api/users/getUser").hasAuthority("ROLE_GET_USER")
                        .requestMatchers(HttpMethod.POST, "/api/auth/login/**").permitAll()
                        .requestMatchers(HttpMethod.POST, "/api/abone/buyPlan").permitAll()
                        .requestMatchers(HttpMethod.DELETE, "/api/abone/{id}").permitAll()
                        .requestMatchers(HttpMethod.PUT, "/api/abone/redaktePlan/{id}").permitAll()
                        .requestMatchers(HttpMethod.GET, "/api/abone/{id}").permitAll()
                        .requestMatchers(HttpMethod.GET, "/api/abone").permitAll()
                        .requestMatchers(HttpMethod.POST, "/api/adminPanel/login").permitAll()
                        .requestMatchers(HttpMethod.POST, "/api/adminPanel/**").hasAuthority("ROLE_ADMIN")
                        .requestMatchers(HttpMethod.GET, "/api/videos/{id}").permitAll()
                        .requestMatchers(HttpMethod.GET, "/api/videos/getAllVideos").permitAll()
                        .requestMatchers(HttpMethod.GET, "/api/videos/getAllWithActive").hasAnyAuthority("ROLE_ADMIN","ROLE_MANAGER")
                        .requestMatchers(HttpMethod.GET, "/api/videos/myVideos").hasAuthority("ROLE_MANAGER")
                        .requestMatchers(HttpMethod.POST, "/api/videos/add").hasAnyAuthority("ROLE_MANAGER","ROLE_ADMIN")
                        .requestMatchers(HttpMethod.PUT, "/api/videos/update/{id}").hasAnyAuthority("ROLE_ADMIN","ROLE_MANAGER")
                        .requestMatchers(HttpMethod.DELETE, "/api/videos/delete/{id}").hasAnyAuthority("ROLE_ADMIN","ROLE_MANAGER")
                        .requestMatchers(HttpMethod.GET, "/api/videos/getByManagerName").permitAll()
                        .requestMatchers(HttpMethod.GET, "/api/videos/findByName").hasAuthority("ROLE_ADMIN")
                        .requestMatchers(HttpMethod.GET, "/api/videos/{ageLimit}").hasAuthority("ROLE_ADMIN")
                        .requestMatchers(HttpMethod.POST,"api/manager/addManager/**").hasAuthority("ROLE_ADMIN")
                        .requestMatchers(HttpMethod.PUT,"/api/manager/{id}/**").hasAuthority("ROLE_ADMIN")
                        .requestMatchers(HttpMethod.DELETE,"/api/manager/deleteManager/{id}/**").hasAuthority("ROLE_ADMIN")
                        .requestMatchers(HttpMethod.GET,"/api/manager/findManager").hasAuthority("ROLE_ADMIN")
                        .requestMatchers(HttpMethod.GET,"/api/users/{id}").hasAuthority("ROLE_ADMIN")
                        .requestMatchers(HttpMethod.GET,"/api/users/isUserExists").hasAuthority("ROLE_ADMIN")
                        .requestMatchers(HttpMethod.GET,"/api/logs").hasAuthority("ROLE_ADMIN")
                        .requestMatchers(permitAllUrls).permitAll()
                        .anyRequest().authenticated())
                .sessionManagement(sess -> sess.sessionCreationPolicy(SessionCreationPolicy.STATELESS))
                .userDetailsService(userDetailsService);
        http.addFilterBefore(jwtFilter, UsernamePasswordAuthenticationFilter.class);
        return http.build();
    }
    @Bean
    public CorsConfigurationSource corsConfigurationSource() {
        CorsConfiguration config = new CorsConfiguration();
        config.setAllowedOrigins(List.of("http://127.0.0.1:5500"));
        config.setAllowedMethods(List.of("*"));
        config.setAllowedHeaders(List.of("*"));
        config.setAllowCredentials(true);

        UrlBasedCorsConfigurationSource source = new UrlBasedCorsConfigurationSource();
        source.registerCorsConfiguration("/**", config);
        return source;
    }

    @Bean
    public AuthenticationManager authenticationManager(AuthenticationConfiguration configuration) throws Exception {
        return configuration.getAuthenticationManager();
    }
    @Bean
    public PasswordEncoder passwordEncoder() {
        return new BCryptPasswordEncoder();
    }
    static String[] permitAllUrls = {
            "/v2/api-docs",
            "/v3/api-docs",
            "/v3/api-docs/**",
            "/swagger-resources",
            "/swagger-resources/**",
            "/configuration/ui",
            "/swagger-ui/**",
            "/swagger-ui.html"
    };

}
